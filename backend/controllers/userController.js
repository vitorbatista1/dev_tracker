const userService = require('../services/userService');
const usuarioSchema = require('../validators/usuarioValidator');

const createUser = async (req, res) => {
  try {
    const { error } = usuarioSchema.validate(req.body, { stripUnknown: true });

    if (error) {
      return res.status(400).json({ error: error.details[0].message });
    }

    const novoUsuario = await userService.createUser(req.body);
    res.status(201).json({ message: 'Usu치rio cadastrado com sucesso!' });
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: 'Erro ao cadastrar usu치rio' });
  }
};


const getAllUsers = async (req, res) => {
  try {
    const usuarios = await userService.getAllUsers();
    res.status(200).json(usuarios);
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: 'Erro ao buscar usu치rios' });
  }
};

const editUser = async (req, res) => {
  try {
    const { error } = usuarioSchema.validate(req.body, { stripUnknown: true });

    if (error) {
      return res.status(400).json({ error: error.details[0].message });
    }

    const updatedUser = await userService.editUser(req.params.id, req.body);
    res.status(200).json(updatedUser);
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: 'Erro ao atualizar usu치rio' });
  }
};

module.exports = {
  createUser,
  getAllUsers,
  editUser
};
